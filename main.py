#Задание 2. Подсчитать четные и нечетные цифры введенного натурального числа.
#Например, если введено число 34560, то у него 3 четные цифры

n = input('Введите  число: ')
even = 0
odd = 0
for numeric in n:
    if int(numeric) % 2 == 0:
        even += 1
    else:
        odd += 1
print(f'Четных цифр: {even}, нечетных цифр: {odd}')

